    <script>
    $(document).ready(function() {
        $('#loginForm').on('submit', function(e) {
            e.preventDefault();
            $.ajax({
                url: 'api/login.php',
                method: 'POST',
                data: $(this).serialize(),
                success: function(response) {
                    if (response.success) {
                        window.location.href = 'dashboard.php';
                    } else {
                        alert('Error de inicio de sesión: ' + response.message);
                    }
                },
                error: function() {
                    alert('Error al intentar iniciar sesión');
                }
            });
        });
    });
